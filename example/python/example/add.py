def add(number_1, number_2):
    """
    Add up two numbers.

    Parameters
    ----------
    number_1 : int
        the first number
    number_2 : int
        the second number

    Returns
    -------
    out : int
        the sum of number_1 and number_2

    """
    return number_1 + number_2
